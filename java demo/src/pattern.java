public class pattern {
    public static void main(String[] args) {
        int n = 5; // You can change this value for the height of the triangle

        for (  int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(i + "");

            }
            System.out.println();
        }
    }
}
