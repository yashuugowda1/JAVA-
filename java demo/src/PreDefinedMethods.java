
public class PreDefinedMethods {
    static void main() {

        System.out.println(Math.abs(-10)); //absolute value
        System.out.println(Math.random()); // prints random number betweem 0.0 to 1.0
        System.out.println(Math.min(10,5)); // prints minimum
        System.out.println(Math.max(10,7)); // prints maximum
        System.out.println(Math.round(10.4)); //roundoff
        System.out.println(Math.pow(2,4)); //power
        System.out.println(Math.sqrt(5)); //square root


    }
}
